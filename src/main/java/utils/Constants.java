package utils;

/**
 * Classe contenant les constantes utilisées dans le jeu.
 * <p>
 * Ces constantes définissent les dimensions de la fenêtre de jeu ainsi que la taille d'une case (tile).
 * </p>
 */
public class Constants {

    /** Largeur de la fenêtre de jeu en pixels. */
    public static final int WINDOW_WIDTH = 719;

    /** Hauteur de la fenêtre de jeu en pixels. */
    public static final int WINDOW_HEIGHT = 575;

    /** Taille d'une case (tile) en pixels, calculée comme la largeur de la fenêtre divisée par 15. */
    public static final int TILE_SIZE = WINDOW_WIDTH / 15;
}
